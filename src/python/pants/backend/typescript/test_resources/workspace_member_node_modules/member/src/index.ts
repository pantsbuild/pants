// Imports clsx which is installed only in member/node_modules/clsx (not at root).
// If pants incorrectly omits member/node_modules from the tsc sandbox, this fails with:
//   TS2307: Cannot find module 'clsx' or its corresponding type declarations.
import { clsx } from 'clsx';

export function buildClassName(...args: Parameters<typeof clsx>): string {
    return clsx(...args);
}
